# Check if the first and last number of a list is the same
# Write a program to print True if the first and last number of a given list is same. If numbers are different then print False.

# define given list
my_list = [10, 20, 30, 20]

# check if the first and last number is same
if my_list[0] == my_list[-1]:
    # using boolean data type
    print(True)
else:
    print(False)
